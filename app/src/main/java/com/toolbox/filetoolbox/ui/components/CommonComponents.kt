/**
 * Reusable filename input row.
 * Shows a default filename with an edit toggle.
 * [defaultName] is the auto-generated base name (without extension).
 * [extension] is the file extension including dot, e.g. ".pdf".
 * [onNameChanged] is invoked with the full filename including extension.
 */
@Composable
fun FileNameInput(
    defaultName: String,
    extension: String,
    onNameChanged: (String) -> Unit,
    modifier: Modifier = Modifier
) {
    var useCustom by remember { mutableStateOf(false) }
    var customName by remember { mutableStateOf("") }

    // Whenever defaultName changes, reset custom
    LaunchedEffect(defaultName) {
        customName = defaultName
        onNameChanged(defaultName + extension)
    }

    Card(
        modifier = modifier.fillMaxWidth(),
        shape = RoundedCornerShape(12.dp),
        colors = CardDefaults.cardColors(
            containerColor = MaterialTheme.colorScheme.surface
        )
    ) {
        Column(modifier = Modifier.padding(16.dp)) {
            Row(
                verticalAlignment = Alignment.CenterVertically
            ) {
                Icon(
                    imageVector = Icons.Default.Edit,
                    contentDescription = null,
                    tint = MaterialTheme.colorScheme.primary,
                    modifier = Modifier.size(18.dp)
                )
                Spacer(modifier = Modifier.width(8.dp))
                Text(
                    text = "文件名",
                    style = MaterialTheme.typography.labelLarge,
                    fontWeight = FontWeight.SemiBold,
                    color = MaterialTheme.colorScheme.onSurface
                )
                Spacer(modifier = Modifier.weight(1f))
                Row(verticalAlignment = Alignment.CenterVertically) {
                    Text(
                        text = "自定义",
                        style = MaterialTheme.typography.labelSmall,
                        color = MaterialTheme.colorScheme.onSurfaceVariant
                    )
                    Spacer(modifier = Modifier.width(4.dp))
                    Switch(
                        checked = useCustom,
                        onCheckedChange = {
                            useCustom = it
                            if (!it) {
                                customName = defaultName
                                onNameChanged(defaultName + extension)
                            }
                        },
                        modifier = Modifier.height(24.dp)
                    )
                }
            }

            Spacer(modifier = Modifier.height(8.dp))

            if (useCustom) {
                OutlinedTextField(
                    value = customName,
                    onValueChange = { v ->
                        customName = v
                        onNameChanged(v + extension)
                    },
                    modifier = Modifier.fillMaxWidth(),
                    singleLine = true,
                    shape = RoundedCornerShape(10.dp),
                    suffix = {
                        Text(
                            text = extension,
                            style = MaterialTheme.typography.bodySmall,
                            color = MaterialTheme.colorScheme.onSurfaceVariant
                        )
                    },
                    textStyle = MaterialTheme.typography.bodyMedium
                )
            } else {
                Text(
                    text = defaultName + extension,
                    style = MaterialTheme.typography.bodySmall,
                    color = MaterialTheme.colorScheme.onSurfaceVariant
                )
            }
        }
    }
}

